export function fetchWeeklySales(year, saleCode) {
  return new Promise((resolve) => {
    const index = weeklySalesDB.findIndex(
      (d) => d.year === year && d.saleCode === saleCode
    );

    if (index === -1) {
      resolve({ currentWeek: null, lastWeek: null });
      return;
    }

    resolve({
      currentWeek: weeklySalesDB[index],
      lastWeek: weeklySalesDB[index - 1] || null,
    });
  });
}
